function loadPageHistory(){
    $(".navbar").attr("style","");
    $(".page").addClass("cached");

    refreshFooterHome();

    if($(".history").length>0){
        $(".history").removeClass("cached");
        initializePageHistory();

    }else{
        mainView.router.reloadPage("ES/history.html");

    }

}

